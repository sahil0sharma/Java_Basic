public class For_Each_Loop {
    public static void main(String[] args){

        int[] arr = new int[10];

        for (int i : arr){
            System.out.println(i);
        }

        //this is for each loop | int represent type of loop
        // i represent the local variable that represent the value of org array
        // arr means which array is talking about
        // only works for array




    }
}
