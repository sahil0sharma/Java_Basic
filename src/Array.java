public class Array {
    public static void main(String[] args) {

        int[] arr1 = new int[5]; // declaration
        arr1[0] = 1; // initailization
        arr1[1] = 2;

        for (int i : arr1){
            System.out.println(i);
        }

        int[] arr = {1, 2, 3, 4, 5, 6, 7};

        for (int i: arr){
            System.out.println(i);
        }


    }
}

